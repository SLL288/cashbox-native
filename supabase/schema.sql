create policy "prototype anon full access project_users" on public.project_users for all to anon using (true) with check (true);
create policy "prototype anon full access daily_cash" on public.daily_cash for all to anon using (true) with check (true);
create policy "prototype anon full access transactions" on public.transactions for all to anon using (true) with check (true);
create policy "prototype anon full access audit_log" on public.audit_log for all to anon using (true) with check (true);

do $$
declare
  table_name text;
begin
  foreach table_name in array array['users', 'projects', 'project_users', 'daily_cash', 'transactions', 'audit_log']
  loop
    if not exists (
      select 1
      from pg_publication_tables
      where pubname = 'supabase_realtime'
        and schemaname = 'public'
        and tablename = table_name
    ) then
      execute format('alter publication supabase_realtime add table public.%I', table_name);
    end if;
  end loop;
end $$;

